package exerciciosAula05;

public class Exercicio2 {

	public static void main(String[] args) {
		int i;
		float f;
		double d;
		
		i = 5;
		f = 5;
		d = 6.6;
		
		System.out.println(i);
		System.out.println(f);
		System.out.println(d);
		
		String stringInteiro = Integer.toString(i);
		String stringFloat = Float.toString(f);
		String stringDouble = Double.toString(d);
		
		System.out.println(stringInteiro);
		System.out.println(stringFloat);
		System.out.println(stringDouble);
		
		
	}

}
